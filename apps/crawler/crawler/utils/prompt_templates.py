"""
Prompt templates for the Football LLM Agent
Contains all the prompts used to instruct the AI on how to process Persian football content
"""

class PromptTemplates:
    """Collection of prompts for football content analysis"""
    
    def get_system_prompt(self) -> str:
        """Main system prompt that defines the AI's role and capabilities"""
        return """
شما یک تحلیلگر حرفه‌ای محتوای فوتبال هستید که در تحلیل اخبار و محتوای فوتبال فارسی تخصص دارید. 
وظیفه شما تجزیه و تحلیل جامع متون فوتبال با استخراج اطلاعات مهم و ساختاریافته است.

قابلیت‌های شما:
1. تحلیل و دسته‌بندی اخبار فوتبال
2. استخراج اطلاعات بازیکنان، تیم‌ها و مسابقات
3. تحلیل احساسات و نظرات
4. خلاصه‌سازی محتوا
5. تشخیص موضوعات و برچسب‌گذاری
6. شناسایی اشخاص و اماکن مهم

اصول کار:
- دقت و صحت در استخراج اطلاعات
- حفظ زمینه فرهنگی و زبانی فارسی
- تفکیک واقعیت از نظر
- ارائه تحلیل منطقی و مستدل
- رعایت اخلاق حرفه‌ای

نتایج خود را به صورت ساختاریافته و قابل پردازش ارائه دهید.
"""
    
    def get_analysis_prompt(self, title: str, content: str) -> str:
        """Main analysis prompt for processing articles"""
        return f"""
لطفاً این مطلب فوتبال را تحلیل کنید:

عنوان: {title}

محتوا: {content}

تحلیل مطلوب:

1. **دسته‌بندی**:
   - نوع مطلب (اخبار، تحلیل، مسابقه، انتقال، مربی، بازیکن)
   - موضوع اصلی

2. **خلاصه**:
   - خلاصه‌ای در 2-3 جمله از محتوا
   - نکات کلیدی و مهم

3. **احساسات**:
   - تحلیل احساسات کلی (مثبت، منفی، خنثی)
   - امتیاز احساسات (-1 تا +1)

4. **موجودیت‌ها**:
   - نام بازیکنان ذکر شده
   - نام تیم‌ها
   - نام مربیان
   - نام لیگ‌ها و مسابقات
   - مکان‌های مهم

5. **اطلاعات بازیکنان**:
   - نام، تیم، پست، سن (در صورت وجود)
   - آمار و عملکرد ذکر شده

6. **اطلاعات مسابقات**:
   - تیم‌های درگیر
   - نتیجه (در صورت وجود)
   - تاریخ و زمان

7. **برچسب‌ها**:
   - کلمات کلیدی مهم
   - موضوعات اصلی

8. **میزان اطمینان**:
   - امتیاز اطمینان به تحلیل (0 تا 1)

لطفاً نتیجه را به صورت JSON یا ساختاریافته ارائه دهید.
"""

